    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
                String supplierID = jTextField1.getText().trim();
                String[] result = Manager3.findSupplierByID(supplierID);
                

                if (result != null) {
                    // Populate the text fields with the supplier's name and items
                    JOptionPane.showMessageDialog(this,supplierID );
                    JOptionPane.showMessageDialog(this,result[0] );
                    JOptionPane.showMessageDialog(this,result[1] );
                    SupplierIDTXT.setText(supplierID);
                    SupplierNametxt.setText(result[1]);
                    ItemSuppliedtxt.setText(result[2]);
                } else {
                    // Clear fields and show a message if the supplier is not found
                    SupplierIDTXT.setText(" ");
                    SupplierNametxt.setText(" ");
                    ItemSuppliedtxt.setText(" ");
                    JOptionPane.showMessageDialog(this, "Supplier not found.");
                }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
            String supplierID = SupplierIDTXT.getText().trim();
            String supplierName = SupplierNametxt.getText().trim();
            String itemsSupplied = ItemSuppliedtxt.getText().trim();
            // Call the delete function from the SupplierManager class
            
            boolean deleted = Manager3.deleteSupplier(supplierID, supplierName, itemsSupplied);

            // Show a message if the supplier was deleted or not found
            if (deleted) {
                JOptionPane.showMessageDialog(this, "Supplier deleted successfully!");
                SupplierIDTXT.setText(" ");
                SupplierNametxt.setText(" ");
                ItemSuppliedtxt.setText(" ");
            } else {
                JOptionPane.showMessageDialog(this, "Supplier not found!");
            }
            
            
      
    }//GEN-LAST:event_jButton4ActionPerformed

