    private void jButton10ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton10ActionPerformed
    try {
        int itemids = Integer.parseInt(jTextField15.getText().trim());
        String[] results = Manager3.finditemByID(itemids);

        if (results != null && results.length == 5) {  // Check if results exist and has 5 elements
            // Displaying the matched item details
            JOptionPane.showMessageDialog(this, "Item ID: " + results[0]);
            JOptionPane.showMessageDialog(this, "Name: " + results[1]);
            JOptionPane.showMessageDialog(this, "Description: " + results[2]);
            JOptionPane.showMessageDialog(this, "Price: " + results[3]);
            JOptionPane.showMessageDialog(this, "Quantity: " + results[4]);

            // Populate the text fields
            jTextField5.setText(results[0]);  // Item ID
            jTextField7.setText(results[1]);  // Name
            jTextField10.setText(results[2]);  // Description
            jTextField11.setText(results[3]);  // Price
            jTextField13.setText(results[4]); 
            // Quantity

        } else {
            // Clear fields and show a message if the supplier is not found
            jTextField2.setText("");
            jTextField3.setText("");
            jTextField4.setText("");
            jTextField6.setText("");
            jTextField9.setText("");

            JOptionPane.showMessageDialog(this, "Item not found.");
    }
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "Please enter a valid number for item ID.");
    }   
    }//GEN-LAST:event_jButton10ActionPerformed

